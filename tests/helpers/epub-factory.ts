/**
 * Test utility for creating minimal EPUB files for unit testing.
 *
 * Creates valid EPUB structures in memory for testing the extractor.
 * Uses the `archiver` library to create ZIP files with EPUB structure.
 */

import { ZipArchive } from 'archiver';

/**
 * Creates a minimal valid EPUB file with the specified metadata.
 *
 * @param options - EPUB metadata options
 * @returns Uint8Array containing the EPUB file
 */
export async function createTestEpub(options: {
  title?: string;
  author?: string;
  coverImage?: Buffer;
  coverFormat?: 'png' | 'jpeg';
  encrypted?: boolean;
  corruptMimetype?: boolean;
  noContainer?: boolean;
  noOpf?: boolean;
  epubVersion?: 2 | 3;
}): Promise<Uint8Array> {
  const {
    title = 'Test Book',
    author = 'Test Author',
    coverImage,
    coverFormat = 'jpeg',
    encrypted = false,
    corruptMimetype = false,
    noContainer = false,
    noOpf = false,
    epubVersion = 3,
  } = options;

  const archive = new ZipArchive({ zlib: { level: 9 } });
  const chunks: Buffer[] = [];

  archive.on('data', (chunk) => chunks.push(chunk));

  // mimetype (must be first, uncompressed)
  archive.append(
    corruptMimetype ? 'application/wrong' : 'application/epub+zip',
    { name: 'mimetype', store: true },
  );

  // container.xml
  if (!noContainer) {
    const containerXml = `<?xml version="1.0" encoding="UTF-8"?>
<container version="1.0" xmlns="urn:oasis:names:tc:opendocument:xmlns:container">
  <rootfiles>
    <rootfile full-path="content.opf" media-type="application/oebps-package+xml"/>
  </rootfiles>
</container>`;
    archive.append(containerXml, { name: 'META-INF/container.xml' });
  }

  // encryption.xml (if encrypted)
  if (encrypted) {
    const encryptionXml = `<?xml version="1.0" encoding="UTF-8"?>
<encryption xmlns="urn:oasis:names:tc:opendocument:xmlns:container">
  <EncryptedData xmlns="http://www.w3.org/2001/04/xmlenc#">
    <CipherReference URI="content.opf"/>
  </EncryptedData>
</encryption>`;
    archive.append(encryptionXml, { name: 'META-INF/encryption.xml' });
  }

  // OPF (package document)
  if (!noOpf) {
    let coverMeta = '';
    let coverItem = '';

    if (coverImage) {
      const coverExt = coverFormat === 'png' ? 'png' : 'jpg';
      const coverMediaType = coverFormat === 'png' ? 'image/png' : 'image/jpeg';

      if (epubVersion === 2) {
        // EPUB2: <meta name="cover" content="cover-image"/>
        coverMeta = `    <meta name="cover" content="cover-image"/>`;
        coverItem = `    <item id="cover-image" href="cover.${coverExt}" media-type="${coverMediaType}"/>`;
      } else {
        // EPUB3: properties="cover-image"
        coverItem = `    <item id="cover-image" href="cover.${coverExt}" media-type="${coverMediaType}" properties="cover-image"/>`;
      }
    }

    const opfXml = `<?xml version="1.0" encoding="UTF-8"?>
<package xmlns="http://www.idpf.org/2007/opf" version="${epubVersion}.0" unique-identifier="bookid">
  <metadata xmlns:dc="http://purl.org/dc/elements/1.1/">
    <dc:title>${title}</dc:title>
    <dc:creator>${author}</dc:creator>
    <dc:identifier id="bookid">urn:uuid:test-book-id</dc:identifier>
    <dc:language>en</dc:language>
${coverMeta}
  </metadata>
  <manifest>
    <item id="chapter1" href="chapter1.xhtml" media-type="application/xhtml+xml"/>
${coverItem}
  </manifest>
  <spine>
    <itemref idref="chapter1"/>
  </spine>
</package>`;
    archive.append(opfXml, { name: 'content.opf' });

    // Cover image
    if (coverImage) {
      const coverExt = coverFormat === 'png' ? 'png' : 'jpg';
      archive.append(coverImage, { name: `cover.${coverExt}` });
    }

    // Minimal chapter
    const chapterXhtml = `<?xml version="1.0" encoding="UTF-8"?>
<!DOCTYPE html>
<html xmlns="http://www.w3.org/1999/xhtml">
<head><title>Chapter 1</title></head>
<body><p>Test content</p></body>
</html>`;
    archive.append(chapterXhtml, { name: 'chapter1.xhtml' });
  }

  await archive.finalize();

  return new Uint8Array(Buffer.concat(chunks));
}

/**
 * Creates a minimal PNG image (1x1 pixel, transparent).
 * For testing cover extraction without needing real image processing.
 */
export function createMinimalPng(): Buffer {
  // Minimal valid PNG: 1x1 transparent pixel
  return Buffer.from([
    0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a, // PNG signature
    0x00, 0x00, 0x00, 0x0d, // IHDR length
    0x49, 0x48, 0x44, 0x52, // IHDR
    0x00, 0x00, 0x00, 0x01, // width: 1
    0x00, 0x00, 0x00, 0x01, // height: 1
    0x08, 0x06, 0x00, 0x00, 0x00, // bit depth, color type, etc.
    0x1f, 0x15, 0xc4, 0x89, // CRC
    0x00, 0x00, 0x00, 0x0a, // IDAT length
    0x49, 0x44, 0x41, 0x54, // IDAT
    0x78, 0x9c, 0x63, 0x00, 0x01, 0x00, 0x00, 0x05, 0x00, 0x01, // compressed data
    0x0d, 0x0a, 0x2d, 0xb4, // CRC
    0x00, 0x00, 0x00, 0x00, // IEND length
    0x49, 0x45, 0x4e, 0x44, // IEND
    0xae, 0x42, 0x60, 0x82, // CRC
  ]);
}

